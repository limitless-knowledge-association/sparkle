#!/usr/bin/env node

/**
 * Bake the version into src/version.js and public/version.js
 * This runs during "npm pack" via the prepack script
 *
 * Generates two version files:
 * - src/version.js: For server-side use (daemon, CLI)
 * - public/version.js: For browser-side use (immediate display in banner)
 */

import { readFile, writeFile } from 'fs/promises';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

async function main() {
  // Read version from package.json
  const packageJsonPath = join(__dirname, '../package.json');
  const packageJson = JSON.parse(await readFile(packageJsonPath, 'utf8'));
  const version = packageJson.version;

  console.log(`[bake-version] Baking version ${version} into version files`);

  const versionFileContent = `// This file is auto-generated by bin/bake-version.js during npm pack
// DO NOT EDIT MANUALLY
export const SPARKLE_VERSION = '${version}';
`;

  // Write version to src/version.js (for server-side)
  const srcVersionPath = join(__dirname, '../src/version.js');
  await writeFile(srcVersionPath, versionFileContent, 'utf8');
  console.log(`[bake-version] ✓ src/version.js (server)`);

  // Write version to public/version.js (for browser-side)
  const publicVersionPath = join(__dirname, '../public/version.js');
  await writeFile(publicVersionPath, versionFileContent, 'utf8');
  console.log(`[bake-version] ✓ public/version.js (browser)`);

  console.log(`[bake-version] Version ${version} baked successfully`);
}

main().catch(error => {
  console.error('[bake-version] Error:', error.message);
  process.exit(1);
});
